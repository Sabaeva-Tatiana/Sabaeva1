# Создай файл fruits.txt и запиши в него три фрукта, каждый с новой строки:
# Яблоко
# Банан
# Апельсин
# Для этой задачи нужно:
# Использовать конструкцию with open()
# Записать несколько строк в файл
# Каждая строка должна быть на новой линии

with open("fruits.txt", "w", encoding="utf-8") as file:
    file.write("Яблоко\nБанан\nАпельсин\n")

# Прочитай файл fruits.txt, который ты создал в предыдущей задаче, и выведи каждую строку из файла отдельно,
# пронумеровав их (1. Яблоко, 2. Банан и т.д.).
# Для этой задачи нужно:
# Прочитать файл построчно
# Использовать цикл для перебора строк
# Пронумеровать вывод строк

with open("fruits.txt", "r", encoding="utf-8") as file:
    lines = file.readlines()
    for i, line in enumerate(lines, 1):
        print(f"{i}. {line.strip()}")

